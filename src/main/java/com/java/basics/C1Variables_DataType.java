package com.java.basics;

public class C1Variables_DataType {

	public static void main(String[] args) {
		// this is comment
		/*
		 * this is multi line comment
		 */

		byte x = 127; // 8 bit
		short s = 32000; // 16bit
		int i = 2140000000; // 32 bit
		long l = 2160000000L; // 64 bit

		long cr = 4444_3333_2222_1111L;
		System.out.println(cr);

		float f = 1.11111111111111111111111F; // 32 bit
		System.out.println(f);

		double d = 1.11111111111111111111111111111; // 64 bit
		System.out.println(d);

		char c = 'x';
		System.out.println(c);

		boolean isThere = true;
		System.out.println(isThere);

		String str = "sudhakar64645@#$";
		System.out.println("my name is " + str);

		Object obj = 10;
		System.out.println(obj);
		
		obj = "selenium";
		System.out.println(obj);
		
	}

}










